game_recipes.forEach((rawRecipe) => {
    if (rawRecipe.outputs.length > 2) {
        return recipes.push(...splitter(rawRecipe))
    }

    recipes.push(remapRecipe(rawRecipe))
})


function remapRecipe(rawRecipe) {
    let ingredients = []
    let outputItem = _.find(game_items, {id: rawRecipe.outputs[0]})
    let outputQuantity = rawRecipe.outputs[1]
    _.chunk(rawRecipe.inputs, 2).forEach((input) => {
        let inputItem = _.find(game_items, {id: input[0]})
        let quantity = input[1]/outputQuantity
        ingredients.push({name: inputItem.name, quantity})
    })
    let altName = rawRecipe.name.includes("(Advanced)") ? rawRecipe.name : null

    return {
        name: outputItem.name,
        altName,
        ingredients
    }
}


function splitter(rawRecipe) {
    if (rawRecipe.name === 'Deuterium Fractionation') {
        let outputItem = _.find(game_items, {id: 1121})
        let inputItem = _.find(game_items, {id: 1120})
        let ingredients = [{name: inputItem.name, quantity: 1}]
        return [{name: outputItem.name, altName: null, ingredients}]
    }

    let recipes = []
    let outputs = _.chunk(rawRecipe.outputs, 2)
    for (let i=0; i<2; i++) {
        let output = outputs[i]
        let input = [...outputs[1-i]]
        input[1] = -input[1]
        let partialRecipe = _.cloneDeep(rawRecipe)
        partialRecipe.outputs = output
        partialRecipe.inputs = [...partialRecipe.inputs, ...input]
        recipes.push(remapRecipe(partialRecipe))
    }


    return recipes
}





